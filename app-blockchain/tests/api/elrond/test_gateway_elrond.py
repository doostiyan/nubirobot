@pytest.mark.slow
class TestGatewayElrondApiCalls(TestCase):
    api = GatewayElrondApi
    addresses = ['erd1swns5yj0vlj05pxx79qafp582mww74lzcwd499lay3z4t3x3sdxsup9fjm',
                 'erd1czuwkkx8a7zyz8pp2zd2nfysra80a9gctm0ar5wnemx0hxdz60zssknnvu']
    txs_hash = ['75171707167a6ade4d142b579b9a6d725e126ab67ad6a3dfb8703bbaa006b1cb',
                'd2cc0655144c0257a37168b4da26b9f736bf4c13c11967657435857f0aaa2487']

    @classmethod
    def check_general_response(cls, response):
        if set(response.keys()) != {'code', 'data', 'error'}:
            return False
        return True

    def test_get_balance_api(self):
        for address in self.addresses:
            get_balance_result = self.api.get_balance(address)
            assert self.check_general_response(get_balance_result)
            assert {'balance'}.issubset(set(get_balance_result.get('data').keys()))
            assert type(get_balance_result.get('data').get('balance')) == str

    def test_get_block_head_api(self):
        get_block_head_response = self.api.get_block_head()
        assert self.check_general_response(get_block_head_response)
        assert {'status'}.issubset(set(get_block_head_response.get('data').keys()))
        assert {'erd_highest_final_nonce'}.issubset(set(get_block_head_response.get('data').get('status').keys()))
        assert type(get_block_head_response.get('data').get('status').get('erd_highest_final_nonce')) == int

    def test_get_tx_details_api(self):
        for tx_hash in self.txs_hash:
            get_tx_details_response = self.api.get_tx_details(tx_hash)
            assert self.check_general_response(get_tx_details_response)
            assert set(get_tx_details_response.get('data').keys()) == {'transaction'}
            assert {'operation', 'receiver', 'sender', 'status', 'value', 'hash', 'timestamp'}.issubset(
                set(get_tx_details_response.get('data').get('transaction')))

    def test_get_address_txs_api(self):
        for address in self.addresses:
            get_address_txs_response = self.api.get_address_txs(address)
            assert self.check_general_response(get_address_txs_response)
            assert set(get_address_txs_response.get('data').keys()) == {'transactions'}
            assert type(get_address_txs_response.get('data').get('transactions')) == list
            for transaction in get_address_txs_response.get('data').get('transactions'):
                assert {'operation', 'receiver', 'sender', 'status', 'value', 'hash', 'timestamp', 'fee'}.issubset(
                    set(transaction.keys()))

    def test_get_block_txs_api(self):
        get_block_txs_response = self.api.get_block_txs(13637338)
        assert self.check_general_response(get_block_txs_response)
        assert set(get_block_txs_response.get('data').keys()) == {'block'}
        mini_blocks = get_block_txs_response.get('data').get('block').get('miniBlocks')
        assert mini_blocks is not None and type(mini_blocks) == list
        for mini_block in mini_blocks:
            assert {'type', 'transactions'}.issubset(set(mini_block.keys()))
            for transaction in mini_block.get('transactions'):
                assert {'sender', 'receiver', 'hash', 'value'}.issubset(set(transaction.keys()))


